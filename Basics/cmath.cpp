#include <cmath>
#include <iostream>
using namespace std;

int main() {
    double floor_result = floor(1.2);  // 1
    double ceil_result = ceil(1.2);    // 2
    double power_result = pow(2, 3);   // 8
    cout << floor_result << endl;
    cout << ceil_result << endl;
    cout << power_result << endl;
    return 0;
}